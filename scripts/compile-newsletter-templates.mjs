function extractBody(html) {
  const match = html.match(/<body[^>]*>([\s\S]*)<\/body>/i);
  return (match ? match[1] : html).trim();
}

const files = readdirSync(SRC_DIR)
  .filter((f) => f.endsWith(".mjml"))
  .sort();

const templates = [];

for (const file of files) {
  const id = file.replace(/\.mjml$/, "");
  const meta = META[id];
  if (!meta) {
    console.warn(`No metadata for ${file}, skipping`);
    continue;
  }

  let source = readFileSync(join(SRC_DIR, file), "utf8");
  source = source.replace(/\{\{SITE\}\}/g, SITE);

  const result = await mjml2html(source, { validationLevel: "soft" });
  const html = result?.html;
  const errors = result?.errors;
  if (errors?.length) {
    console.warn(`${file}:`, errors.map((e) => e.message || e).join("; "));
  }
  if (!html) {
    console.error(`Failed to compile ${file}`);
    process.exit(1);
  }

  templates.push({
    id,
    ...meta,
    mjml: file,
    html: extractBody(html),
  });
}

const out = `// AUTO-GENERATED by scripts/compile-newsletter-templates.mjs — do not edit by hand.
// Rebuild: npm run build:templates

/** @type {{ id: string, name: string, description: string, category: string, mjml: string, html: string }[]} */
export const NEWSLETTER_TEMPLATES = ${JSON.stringify(templates, null, 2)};
`;

writeFileSync(OUT, out);
console.log(`Compiled ${templates.length} templates → js/newsletter-templates.js`);
